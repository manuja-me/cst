#include <stdio.h>

int main() {
  printf("Escape Sequences Meaning\n");
  printf("---------------------------------\n");
  printf("\\\\ \t Back slash\n");
  printf("\\\' \t Single Quotatitons\n");
  printf("\\\" \t Double Quotatitons\n");
  printf("\\n \t New Line (Line feed & carriage return)\n");
  printf("\\t \t Tab\n");
  printf("\\b \t Back Space\n");
  printf("\\r \t Carriage Return\n");

  return 0;
}
